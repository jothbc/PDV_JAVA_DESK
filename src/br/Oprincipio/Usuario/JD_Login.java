    private void ok() {
        String login,senha;
        login = logintxt.getText().toUpperCase();
        senha = new String(senhatxt.getPassword());
        Usuario_Bean temp = new Usuario_DAO().getUsuario(login);
        if (temp == null){
            JOptionPane.showMessageDialog(null, "Usuario n√£o cadastrado.");
        }
        else if (temp.getSenha().equals(CriptografiaMD5.criptografar(senha))){
            usuario = temp;
            this.dispose();
        }else{
            JOptionPane.showMessageDialog(null, "Senha incorreta.");
        }
    }

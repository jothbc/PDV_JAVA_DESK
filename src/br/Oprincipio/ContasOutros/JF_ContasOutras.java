    private void novo() {
        JD_ContasNovo jd = new JD_ContasNovo(this, true);
        jd.setVisible(true);
        atualizarTabela();
    }

    private void atualizarTabela() {
        tb.setRowCount(0);
        List<Conta_Bean> contas = new Conta_DAO().findAll();
        for (Conta_Bean c : contas) {
            Object[] dado = {c.getId(), c.getDescricao(), c.getValor(), c.getVencimento(Conta_Bean.BR), c.getPago(Conta_Bean.BR)};
            tb.addRow(dado);
        }
        valorAberto();
    }

    private void delete() {
        int op = -1;
        op = JOptionPane.showOptionDialog(this, "Deseja realmente excluir a conta selecionada?", "Confirmação de Exclusão", JOptionPane.YES_NO_CANCEL_OPTION, JOptionPane.WARNING_MESSAGE, null, null, null);
        if (op != 0) {
            return;
        }
        int id = (int) jTable1.getValueAt(jTable1.getSelectedRow(), 0);
        if (!new Conta_DAO().removerConta(id)) {
            JOptionPane.showMessageDialog(null, "Erro ao tentar excluir conta.", "Erro", JOptionPane.ERROR_MESSAGE);
        } else {
            atualizarTabela();
        }
    }

    private void pagar() {
        int id = (int) jTable1.getValueAt(jTable1.getSelectedRow(), 0);
        if (!new Conta_DAO().pagarConta(id)) {
            JOptionPane.showMessageDialog(null, "Erro ao tentar pagar conta.", "Erro", JOptionPane.ERROR_MESSAGE);
        } else {
            atualizarTabela();
        }
    }

    public void valorAberto() {
        double valor = 0;
        for (int x = 0; x < jTable1.getRowCount(); x++) {
            if (jTable1.getValueAt(x, 4) == null) {
                double temp = (double) jTable1.getValueAt(x, 2);
                valor+=temp;
            }
        }
        abertotxt.setText("Em Aberto: R$"+Conv.colocarPontoEmValor(Conv.CDblDuasCasasString(valor).replaceAll("\\.", ",")));
    }

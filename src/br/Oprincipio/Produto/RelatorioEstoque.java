    private void ini() {
        tb = (DefaultTableModel) jTable1.getModel();
        BG.add(EA);
        EA.setSelected(true);
        BG.add(EP);
        BG.add(EN);
        BG.add(EAM);
        produto = null;
        venda = null;
    }
    
    private void gerar() {
        List<Produto_Bean> produtos = new Produto_DAO().findAll();
        tb.setRowCount(0);
        if (EA.isSelected()) {
            for (Produto_Bean p : produtos) {
                Object[] dado = {p.getCodigo(), p.getDescricao(), p.getCusto(), p.getVenda(), p.getEstoque_min(), p.getEstoque()};
                tb.addRow(dado);
            }
        } else if (EP.isSelected()) {
            for (Produto_Bean p : produtos) {
                if (p.getEstoque() >= 0) {
                    Object[] dado = {p.getCodigo(), p.getDescricao(), p.getCusto(), p.getVenda(), p.getEstoque_min(), p.getEstoque()};
                    tb.addRow(dado);
                }
            }
        } else if (EN.isSelected()) {
            for (Produto_Bean p : produtos) {
                if (p.getEstoque() < 0) {
                    Object[] dado = {p.getCodigo(), p.getDescricao(), p.getCusto(), p.getVenda(), p.getEstoque_min(), p.getEstoque()};
                    tb.addRow(dado);
                }
            }
        } else if (EAM.isSelected()) {
            for (Produto_Bean p : produtos) {
                if (p.getEstoque() < p.getEstoque_min()) {
                    Object[] dado = {p.getCodigo(), p.getDescricao(), p.getCusto(), p.getVenda(), p.getEstoque_min(), p.getEstoque()};
                    tb.addRow(dado);
                }
            }
        }
    }

    private void imprimir() {
        if (EA.isSelected()) {
            ImprimirIreport.thisRelatorioEstoque(1);
        } else if (EP.isSelected()) {
            ImprimirIreport.thisRelatorioEstoque(2);
        } else if (EN.isSelected()) {
            ImprimirIreport.thisRelatorioEstoque(3);
        } else if (EAM.isSelected()) {
            ImprimirIreport.thisRelatorioEstoque(4);
        }
    }

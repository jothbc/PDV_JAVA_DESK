    private void excluirBtn() {
        if (existe) {
            int op = -1;
            op = JOptionPane.showOptionDialog(null, "Deseja realmente Excluir Esse produto?", "Exclusão Segura", JOptionPane.YES_NO_CANCEL_OPTION, op, null, null, null);
            if (op == 0) {
                if (new Produto_DAO().delete(Integer.parseInt(cdInternotxt.getText()))) {
                    JOptionPane.showMessageDialog(null, "Produto excluido com sucesso!");
                } else {
                    JOptionPane.showMessageDialog(null, "Erro ao tentar exluir o produto do banco de dados!");
                }
            }
        }
        cancelarBtn();
    }


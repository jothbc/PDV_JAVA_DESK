    private void gravarBtn() {
        try {
            Produto_Bean p = new Produto_Bean();
            p.setId(Integer.parseInt(cdInternotxt.getText()));
            p.setCodigo(cd_tf.getText());
            p.setDescricao(desctf.getText());
            if (!custotf.getText().equals("")) {
                p.setCusto(Conv.CDblDuasCasas(Double.parseDouble(custotf.getText().replaceAll(",", "\\."))));
            }
            if (!lucrotf.getText().equals("")) {
                p.setMargem(Conv.CDblDuasCasas(Double.parseDouble(lucrotf.getText().replaceAll(",", "\\."))));
            }
            if (!vendatf.getText().equals("")) {
                p.setVenda(Conv.CDblDuasCasas(Double.parseDouble(vendatf.getText().replaceAll(",", "\\."))));
            }
            if (!estoquetf.getText().equals("")) {
                p.setEstoque(Conv.CDblDuasCasas(Double.parseDouble(estoquetf.getText().replaceAll(",", "\\."))));
            }
            if (!mintf.getText().equals("")) {
                p.setEstoque_min(Conv.CDblDuasCasas(Double.parseDouble(mintf.getText().replaceAll(",", "\\."))));
            }
            if (!existe) {
                if (new Produto_DAO().insertInto(p)) {
                    JOptionPane.showMessageDialog(null, "Produto Cadastrado com sucesso!");
                    produtos = new Produto_DAO().findAll();
                } else {
                    JOptionPane.showMessageDialog(null, "Erro ao cadastrar o Produto!");
                }
            } else if (existe) {
                if (new Produto_DAO().update(p)) {
                    JOptionPane.showMessageDialog(null, "Produto Atualizado com sucesso!");
                    produtos = new Produto_DAO().findAll();
                } else {
                    JOptionPane.showMessageDialog(null, "Erro ao atualizar o Produto!");
                }
            }
        } catch (Exception ex) {
            Logger.getLogger(JF_CadastroProduto.class.getName()).log(Level.SEVERE, null, ex);
        }
        cancelarBtn();
    }


    private void incluir() {
        Produto_Bean p = new Produto_DAO().getProduto_COD(cdtxt.getText());
        if (p != null) {
            double qtd = Conv.CDblDuasCasas(Double.parseDouble(qtdtxt.getText().replaceAll(",", "\\.")) * Double.parseDouble(mult.getText().replaceAll(",", "\\.")));
            Object[] dado = {p.getCodigo(), p.getDescricao(), qtd};
            tb.addRow(dado);
            cdtxt.setText("");
            qtdtxt.setText("1");
            mult.setText("1");
            cdtxt.requestFocus();
        }
        if (p == null) {
            JOptionPane.showMessageDialog(null, "Produto/Combo não cadastrado!", "Não cadastrado", JOptionPane.INFORMATION_MESSAGE);
        }
    }

    private void excluir() {
        if (jTable1.getSelectedRow() >= 0) {
            tb.removeRow(jTable1.getSelectedRow());
        }
    }

    private void salvar() {
        jProgressBar1.setMinimum(0);
        jProgressBar1.setMaximum(tb.getRowCount());
        jProgressBar1.setValue(0);
        for (int x = 0; x < tb.getRowCount(); x++) {
            String cd = (String) tb.getValueAt(x, 0);
            double qtd = (double) tb.getValueAt(x, 2);
            if (new Produto_DAO().thisIsCombo(cd)) {
                if (!new Produto_DAO().entradaCombo(cd, qtd)) {
                    JOptionPane.showMessageDialog(null, "Erro ao tentar dar entrada no combo: " + cd + "\nOs itens subsequentes serão abortados.", "Erro", JOptionPane.ERROR_MESSAGE);
                    return;
                }
            }
            else if (!new Produto_DAO().entradaItem(cd, qtd)) {
                JOptionPane.showMessageDialog(null, "Erro ao tentar dar entrada no item: " + cd + "\nOs itens subsequentes serão abortados.", "Erro", JOptionPane.ERROR_MESSAGE);
                return;
            }
            jProgressBar1.setValue(x);
        }
        tb.setRowCount(0);
        jProgressBar1.setValue(0);
        cdtxt.requestFocus();
    }

    private void buscar() {
        JD_BuscarProduto jd = new JD_BuscarProduto(this, true, 1);
        jd.setVisible(true);
        if (jd.p != null) {
            cdtxt.setText(jd.p.getCodigo());
            qtdtxt.setText("1");
            qtdtxt.requestFocus();
        }
    }
    

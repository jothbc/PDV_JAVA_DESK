    public JD_BuscarProduto(java.awt.Frame parent, boolean modal, int opcao) {
        super(parent, modal);
        initComponents();
        List<Produto_Bean> temp = new Produto_DAO().findAll();
        produtos = new ArrayList<>();
        for (Produto_Bean p_temp : temp) {
            if (opcao == 1) {
                if (!new Produto_DAO().thisIsCombo(p_temp.getCodigo())) {
                    produtos.add(p_temp);
                }
            } else {
                if (new Produto_DAO().thisIsCombo(p_temp.getCodigo())) {
                    produtos.add(p_temp);
                }
            }
        }
        tb = (DefaultTableModel) jTable1.getModel();
        jTable1.setRowSorter(new TableRowSorter<>(tb));
        preencherTB();
        p = null;
        buscatxt.requestFocus();
    }


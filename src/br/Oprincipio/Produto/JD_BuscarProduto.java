    private void preencherTB() {
        tb.setRowCount(0);
        for (Produto_Bean p : produtos) {
            Object[] dado = {p.getId(), p.getCodigo(), p.getDescricao(), p.getCusto(), p.getMargem(), p.getVenda(), p.getEstoque(), p.getEstoque_min()};
            tb.addRow(dado);
        }

    }

    private void retornarProduto() {
        try {
            Produto_Bean produto = new Produto_Bean();
            int index = jTable1.getSelectedRow();
            produto.setId((int) jTable1.getValueAt(index, 0));
            produto.setCodigo((String) jTable1.getValueAt(index, 1));
            produto.setDescricao((String) jTable1.getValueAt(index, 2));
            produto.setCusto((double) jTable1.getValueAt(index, 3));
            produto.setMargem((double) jTable1.getValueAt(index, 4));
            produto.setVenda((double) jTable1.getValueAt(index, 5));
            produto.setEstoque((double) jTable1.getValueAt(index, 6));
            produto.setEstoque_min((double) jTable1.getValueAt(index, 7));
            p = produto;
            this.dispose();
        } catch (Exception ex) {
            Logger.getLogger(JF_BuscarProduto.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

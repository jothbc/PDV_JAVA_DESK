    private void jTable1KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTable1KeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            if (jTable1.getSelectedRow() >= 0) {
                retornarProduto();
            }
        }
    }//GEN-LAST:event_jTable1KeyPressed

    private void buscatxtKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_buscatxtKeyPressed
        String compara = buscatxt.getText();
        tb.setRowCount(0);
        for (Produto_Bean p : produtos) {
            Object[] dado = {p.getId(), p.getCodigo(), p.getDescricao(), p.getCusto(), p.getMargem(), p.getVenda(), p.getEstoque(), p.getEstoque_min()};
            if (opCBox.getSelectedIndex() == 0 && p.getCodigo().contains(compara)) {
                tb.addRow(dado);
            } else if (opCBox.getSelectedIndex() == 1 && p.getDescricao().contains(compara)) {
                tb.addRow(dado);
            }
        }
    }//GEN-LAST:event_buscatxtKeyPressed


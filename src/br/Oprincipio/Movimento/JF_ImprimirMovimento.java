    private void visualizar() {
        try {
            Venda_Bean venda = new Venda_DAO().getVenda(Integer.parseInt(jTextField1.getText()));
            if (venda.getMovimento() != 0) {
                JD_Movimento jd = new JD_Movimento(null, true, venda);
                jd.setVisible(true);
            } else {
                JOptionPane.showMessageDialog(null, "Venda n√£o encontrada.");
            }
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, "Valor de movimento incorreto.");
        }
    }

    private void imprimir() {
        try {
            ImprimirIreport.thisMov(Integer.parseInt(jTextField1.getText()));
        }catch(NumberFormatException ex){
            JOptionPane.showMessageDialog(null, "Valor de movimento incorreto.");
        }
    }

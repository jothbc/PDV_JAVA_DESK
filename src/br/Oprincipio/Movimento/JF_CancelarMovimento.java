    private void jradioSelected() {
        if (jRadioButton1.isSelected()) {
            preencherTB(1);
        }
        if (jRadioButton2.isSelected()) {
            preencherTB(2);
        }
        if (jRadioButton3.isSelected()) {
            preencherTB(3);
        }
    }

    private void preencherTB(int i) {
        tb.setRowCount(0);
        switch (i) {
            case 1:
                preencherMovimento();
                break;
            case 2:
                preencherData();
                break;
            case 3:
                preencherItem();
                break;
            default:
                break;
        }
    }

    private void preencherMovimento() {
        if ("".equals(mov.getText())) {
            return;
        }
        int movimento = Integer.parseInt(mov.getText());
        Venda_Bean v = new Venda_DAO().getVenda(movimento);
        Object[] dado = {v.getMovimento(), CDate.DataMySQLtoDataStringPT(v.getData()), v.getCliente().getNome(), v.getTotal()};
        tb.addRow(dado);
    }

    private void preencherData() {
        try {
            List<Venda_Bean> vendas = new Venda_DAO().getVendasData(CDate.DataPTBRtoDataMySQL(data.getText()));
            for (Venda_Bean v : vendas) {
                Object[] dado = {v.getMovimento(), CDate.DataMySQLtoDataStringPT(v.getData()), v.getCliente().getNome(), v.getTotal()};
                tb.addRow(dado);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Data inv√°lida\n" + ex, "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void preencherItem() {
        List<Venda_Bean> vendas = new Venda_DAO().histVendaProduto(prod.getText());
        for (Venda_Bean v : vendas) {
            Object[] dado = {v.getMovimento(), v.getData(), v.getCliente().getNome(), v.getTotal()};
            tb.addRow(dado);
        }
    }

    private void buscar() {
        JD_BuscarProduto jd = new JD_BuscarProduto(this, true, 1);
        jd.setVisible(true);
        if (jd.p != null) {
            prod.setText(jd.p.getCodigo());
        }
    }

    private void ir() {
        jradioSelected();
    }

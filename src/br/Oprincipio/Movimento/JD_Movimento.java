    private void preencherTB() {
        tb.setRowCount(0);
        if (venda != null) {
            for (Produto_Bean p : venda.getProdutos()) {
                Object[] dado = {p.getCodigo(), p.getDescricao(), Conv.validarValue(p.getVenda()), p.getQuantidade(), Conv.validarValue(p.getVenda() * p.getQuantidade())};
                tb.addRow(dado);
            }
            venda.getPos().getParcela().forEach((p) -> {
                parcelasCombo.addItem(p.getData()+"  R$ "+p.getValor());
            });
            movlbl.setText("MOVIMENTO "+venda.getMovimento()+ "          "+venda.getData()
            +"         "+ venda.getCliente().getNome());
            if (venda.getData_pago()!=null){
                data_crediariotxt.setText(venda.getData_pago());
            }
            totaltf.setText(Conv.validarValue(venda.getTotal()));
            descontotf.setText(Conv.validarValue(venda.getDesconto()));
            trocotf.setText(Conv.validarValue(venda.getTroco()));
            dinheirotf.setText(Conv.validarValue(venda.getDinheiro()));
            posCreditotf.setText(Conv.validarValue(venda.getPOS_credito()));
            posDebitotf.setText(Conv.validarValue(venda.getPOS_debito()));
            crediariotf.setText(Conv.validarValue(venda.getCrediario()));
        }
    }


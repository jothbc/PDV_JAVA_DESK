    private void preencherTB() {
        tb.setRowCount(0);
        if (venda != null) {
            for (Produto_Bean p : venda.getProdutos()) {
                String cancelado = null;
                if (p.isCancelado()){
                    cancelado = "cancelado";
                }
                Object[] dado = {p.getCodigo(), p.getDescricao(), Conv.validarValue(p.getVenda()), p.getQuantidade(), Conv.validarValue(p.getVenda() * p.getQuantidade()),cancelado};
                tb.addRow(dado);
            }
            movlbl.setText("MOVIMENTO " + venda.getMovimento());
        }
    }

    private void cancelarItem() {
        if (controle < Double.parseDouble(qtd.getText().replaceAll(",", "\\."))){
            JOptionPane.showMessageDialog(null, "Quantidade maior do que a vendida.");
            return;
        }
        if (jTable1.getValueAt(jTable1.getSelectedRow(), 5) == "cancelado"){
            JOptionPane.showMessageDialog(null, "Item já cancelado.");
            return;
        }
        int op = -1;
        op = JOptionPane.showOptionDialog(this, "Sera feito um estorno da quantidade informada para o estoque e o item sera cancelado.\nNão será possivel desfazer o cancelamento, nen mudar a quantidade de estorno depois.\nConfirmar?", "Cofirmação de Cancelamento de Item", JOptionPane.YES_NO_CANCEL_OPTION, JOptionPane.WARNING_MESSAGE , null, null, null);
        if (op!=0){
            return;
        }
        
        if (new Venda_DAO().cancelarItem(venda,item.getText(),Double.parseDouble(qtd.getText().replaceAll(",", "\\.")))){
            for (Produto_Bean p:venda.getProdutos()){
                if (p.getCodigo().equals(item.getText())){
                    p.setCancelado(true);
                }
            }
            preencherTB();
        }
    }

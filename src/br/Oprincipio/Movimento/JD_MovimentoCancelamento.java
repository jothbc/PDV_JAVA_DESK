    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        if (jTable1.getSelectedRow() < 0) {
            return;
        }
        if (evt.getClickCount()==1){
            item.setText((String) jTable1.getValueAt(jTable1.getSelectedRow(), 0));
            qtd.setText(Conv.CDblDuasCasasString((double) jTable1.getValueAt(jTable1.getSelectedRow(), 3)));
            controle = Double.parseDouble(qtd.getText());
        }
        if (evt.getClickCount() == 2) {
            if (new Produto_DAO().thisIsCombo((String) jTable1.getValueAt(jTable1.getSelectedRow(), 0))) {
                Produto_Bean p = new Produto_DAO().getProduto_COD((String) jTable1.getValueAt(jTable1.getSelectedRow(), 0));
                Combo_Bean c = new Produto_DAO().getCombo(p.getCodigo(), p.getDescricao(), p.getId(), p.getVenda(), p.getMargem());
                JOptionPane.showMessageDialog(null, c.toString());
            }
        }
        

    }//GEN-LAST:event_jTable1MouseClicked


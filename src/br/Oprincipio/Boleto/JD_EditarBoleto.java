    private void capturarBoleto() {
        Boleto_Bean boleto = new Boleto_DAO().getBoletoByID(idBoleto);
        forntxt.setText(boleto.getFornecedor().getNome());
        valortxt.setText(Double.toString(boleto.getValor()));
        vencimentotxt.setText(boleto.getVencimento());
        codigotxt.setText(boleto.getCd_barras());
    }

    private void gravar() {
        if (vencimentotxt.getText().equals("  /  /    ") || valortxt.getText().equals("") || codigotxt.getText().length()!=44){
            return;
        }
        Fornecedor_Bean f = new Fornecedor_DAO().getFornecedorByNome(forntxt.getText());
        Boleto_Bean b = new Boleto_DAO().getBoletoByID(idBoleto);
        b.setFornecedor(f);
        b.setValor(Conv.CDblDuasCasas(Double.parseDouble(valortxt.getText().replaceAll(",", "\\."))));
        b.setVencimento(vencimentotxt.getText());
        if (new Boleto_DAO().updateBoleto(b)){
            this.dispose();
        }
        else{
            JOptionPane.showMessageDialog(null, "Algo deu errado ao tentar atualizar o boleto!","Erro",JOptionPane.ERROR_MESSAGE);
        }
    }

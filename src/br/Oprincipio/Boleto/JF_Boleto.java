    private void analisarCodigoDeBarras(String codigo) {
        try {
            Boleto_Bean b = new Boleto_Bean();
            b.setAll(codigo);
            bancotxt.setText(Integer.toString(b.getBanco()));
            vencimento_boleto_txt.setText(b.getVencimento());
            valor_boleto_txt.setText(Conv.validarValue(b.getValor()));
            cd_barras_leitor.setText(b.getCd_barras());
            buscar_fornecedor();
            if (jComboBoxFornecedor.getItemCount() == 0) {
                JD_BuscaFornecedorBanco jd = new JD_BuscaFornecedorBanco(this, true, b.getBanco(), b.getCd_barras());
                jd.setVisible(true);
                jComboBoxFornecedor.removeAllItems();
                jComboBoxFornecedor.addItem(jd.fornecedor_retorno);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex, "Erro", JOptionPane.ERROR_MESSAGE);
            System.out.println(ex);
        }
    }

    private void valorMenos() {
        if (cd_barras_leitor.getText().length() != 44 && cd_barras_leitor.getText().length() != 47) {
            JOptionPane.showMessageDialog(null, "Algo esta errado com o cÃ³digo de barras.\nTamanho esperado: 44 ou 47.\nTamanho informado: " + cd_barras_leitor.getText().length());
        }
        String cd_barras = cd_barras_leitor.getText();
        if (cd_barras.length() == 47) {
            String temp = "";
            temp = temp + cd_barras.substring(0, 4);
            temp = temp + cd_barras.substring(32);
            temp = temp + cd_barras.substring(4, 9);
            temp = temp + cd_barras.substring(10, 20);
            temp = temp + cd_barras.substring(21, 31);
            cd_barras = temp;
        }
        localValor++;
        double valor = Double.parseDouble(cd_barras.substring(localValor, 10 + localValor)) / 100;
        valor_boleto_txt.setText(Conv.validarValue(valor));
    }

    private void valorMais() {
        if (cd_barras_leitor.getText().length() != 44 && cd_barras_leitor.getText().length() != 47) {
            JOptionPane.showMessageDialog(null, "Algo esta errado com o cÃ³digo de barras.\nTamanho esperado: 44 ou 47.\nTamanho informado: " + cd_barras_leitor.getText().length());
        }
        String cd_barras = cd_barras_leitor.getText();
        if (cd_barras.length() == 47) {
            String temp = "";
            temp = temp + cd_barras.substring(0, 4);
            temp = temp + cd_barras.substring(32);
            temp = temp + cd_barras.substring(4, 9);
            temp = temp + cd_barras.substring(10, 20);
            temp = temp + cd_barras.substring(21, 31);
            cd_barras = temp;
        }
        localValor--;
        double valor = Double.parseDouble(cd_barras.substring(localValor, 10 + localValor)) / 100;
        valor_boleto_txt.setText(Conv.validarValue(valor));
    }

    private void buscar_fornecedor() {
        String cd = cd_barras_leitor.getText();
        jComboBoxFornecedor.removeAllItems();
        if (cd.length() != 44) {
            return;
        }
        List<Fornecedor_Bean> fornecedores = new Fornecedor_DAO().findAll();
        for (Fornecedor_Bean f : fornecedores) {
            for (Banco_Bean b : f.getBanco()) {
                if (b.getNumero() != null) {
                    if (cd.contains(b.getNumero())) {
                        jComboBoxFornecedor.addItem(f.getNome());
                    }
                }
            }
        }
    }

    private void salvarBoleto() {
        if (cd_barras_leitor.getText().length() == 44) {
            List<Fornecedor_Bean> fornecedores = new Fornecedor_DAO().getFornecedoresSeco();
            Fornecedor_Bean f = null;
            for (Fornecedor_Bean forn : fornecedores) {
                if (forn.getNome().equals(jComboBoxFornecedor.getItemAt(jComboBoxFornecedor.getSelectedIndex()))) {
                    f = forn;
                }
            }
            List<Boleto_Bean> boletos = new Boleto_DAO().findAll();
            for (Boleto_Bean b : boletos) {
                if (b.getCd_barras().equals(cd_barras_leitor.getText())) {
                    JOptionPane.showMessageDialog(null, "CÃ³digo de boleto jÃ¡ lanÃ§ado", "Erro", JOptionPane.ERROR_MESSAGE);
                    return;
                }
            }
            if (f != null) {
                Boleto_Bean b = new Boleto_Bean();
                b.setCd_barras(cd_barras_leitor.getText());
                b.setBanco(Integer.parseInt(bancotxt.getText()));
                b.setVencimento(vencimento_boleto_txt.getText());
                b.setValor(Conv.CDblDuasCasas(Double.parseDouble(valor_boleto_txt.getText().replaceAll(",", "\\."))));
                b.setFornecedor(f);
                if (!new Boleto_DAO().addBoleto(b)) {
                    JOptionPane.showMessageDialog(null, "Erro ao tentar salvar o boleto.", "ERRO", JOptionPane.ERROR_MESSAGE);
                } else {
                    atualizarTB();
                    limparTXTS();
                    int maior = 0;
                    int value = 0;
                    for (int x = 0; x < jTable1.getRowCount(); x++) {
                        if ((int) jTable1.getValueAt(x, 0) > value) {
                            value = (int) jTable1.getValueAt(x, 0);
                            maior = x;
                        }
                    }
                    jTable1.setRowSelectionInterval(maior, maior);
                    ScroollJTable.scrollToVisible(jTable1, maior);
                }
            }
        }
    }

    private void atualizarTB() {
        tb.setRowCount(0);
        List<Boleto_Bean> boletos = new Boleto_DAO().findAll();
        for (Boleto_Bean b : boletos) {
            //"Sequencia", "Vencimento", "Fornecedor_Bean", "Valor", "Pago", "CÃ³digo de Barras", "Marcar"
            Date vencimento, pago = null;
            SimpleDateFormat form = new SimpleDateFormat("dd/MM/yyyy");
            try {
                vencimento = form.parse(b.getVencimento());
            } catch (ParseException ex) {
                vencimento = null;
                Logger.getLogger(JF_Boleto.class.getName()).log(Level.SEVERE, null, ex);
            }
            if (b.getPago() != null) {
                try {
                    pago = form.parse(b.getPago());
                } catch (ParseException ex) {
                    Logger.getLogger(JF_Boleto.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else {
                pago = null;
            }
            Object[] dado = {b.getId(), vencimento, b.getFornecedor().getNome(), b.getValor(), pago, b.getCd_barras(), null};
            if (inicio.getText().equals("  /  /    ") && fim.getText().equals("  /  /    ")) {
                if (CheckBoxPagos.isSelected()) {
                    tb.addRow(dado);
                } else if (pago == null) {
                    tb.addRow(dado);
                }
            } else {
                Date dboleto = vencimento;
                Date ini;
                Date fi;
                try {
                    ini = form.parse(inicio.getText());
                    fi = form.parse(fim.getText());
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(null, "Filtro de datas invÃ¡lidos.");
                    return;
                }
                if (dboleto.compareTo(ini) >= 0 && dboleto.compareTo(fi) <= 0) {
                    if (CheckBoxPagos.isSelected()) {
                        tb.addRow(dado);
                    } else if (pago == null) {
                        tb.addRow(dado);
                    }
                }
            }
        }
        total_em_aberto();
        total_em_aberto_tabela();
        totalLinhas.setText("Total de Linhas Exibidas: "+tb.getRowCount());
    }

    public void limparTXTS() {
        cd_barras_leitor.setText("");
        jComboBoxFornecedor.removeAllItems();
        bancotxt.setText("");
        vencimento_boleto_txt.setText("");
        valor_boleto_txt.setText("");
        cd_barras_leitor.requestFocus();
    }

    private void clickNaTabela() {
        if (jTable1.getSelectedRow() < 0) {
            return;
        }
        if (jTable1.getValueAt(jTable1.getSelectedRow(), 6) == null) {
            jTable1.setValueAt("X", jTable1.getSelectedRow(), 6);
        } else {
            jTable1.setValueAt(null, jTable1.getSelectedRow(), 6);
        }
    }

    private void baixar_selecionado() {
        for (int x = 0; x < jTable1.getRowCount(); x++) {
            if (jTable1.getValueAt(x, 6) != null) {
                if (!new Boleto_DAO().pagarBoleto((int) jTable1.getValueAt(x, 0))) {
                    JOptionPane.showMessageDialog(null, "Erro ao tentar dar baixa no boleto.");
                    return;
                }
            }
        }
        atualizarTB();
    }

    private void baixar_todos() {
        int op = -1;
        op = JOptionPane.showOptionDialog(this, "Deseja realmente sinalizar como pago todos os boletos exibidos na tabela?", "ConfirmaÃ§Ã£o de SeguranÃ§a.", JOptionPane.YES_NO_CANCEL_OPTION, JOptionPane.WARNING_MESSAGE, null, null, null);
        if (op == 0) {
            for (int x = 0; x < jTable1.getRowCount(); x++) {
                if (!new Boleto_DAO().pagarBoleto((int) jTable1.getValueAt(x, 0))) {
                    JOptionPane.showMessageDialog(null, "Erro ao tentar dar baixa no boleto.");
                    return;
                }
            }
            atualizarTB();
        }
    }

    private void total_em_aberto() {
        List<Boleto_Bean> total = new Boleto_DAO().findAll();
        double valor = 0;
        for (Boleto_Bean b : total) {
            if (b.getPago() == null) {
                valor += b.getValor();
            }
        }
        valorTotalEmAbertotxt.setText(Conv.colocarPontoEmValor(Conv.validarValue(valor)));
    }

    private void total_em_aberto_tabela() {
        double valor = 0;
        int count =0;
        for (int x = 0; x < jTable1.getRowCount(); x++) {
            if (jTable1.getValueAt(x, 4) == null) {
                valor += (double) jTable1.getValueAt(x, 3);
                count++;
            }
        }
        valorAbertotxt.setText(Conv.colocarPontoEmValor(Conv.validarValue(valor)));
        totalLinhasAberto.setText("Total de Linhas em Aberto: "+count);
    }

    private void editarBoleto() {
        if (jTable1.getSelectedRow() >= 0) {
            int id = (int) jTable1.getValueAt(jTable1.getSelectedRow(), 0);
            JD_EditarBoleto jd = new JD_EditarBoleto(this, true, id);
            jd.setVisible(true);
            atualizarTB();
        }
    }

    private void excluir() {
        int op = -1;
        op = JOptionPane.showOptionDialog(this, "Deseja realmente excluir o boleto selecionado?", "ConfirmaÃ§Ã£o de exclusÃ£o", JOptionPane.YES_NO_CANCEL_OPTION, JOptionPane.WARNING_MESSAGE, null, null, null);
        if (op != 0) {
            return;
        }
        int id = (int) jTable1.getValueAt(jTable1.getSelectedRow(), 0);
        if (!new Boleto_DAO().deletarBoleto(id)){
            JOptionPane.showMessageDialog(null, "Erro ao tentar deletar boleto!","Erro",JOptionPane.ERROR_MESSAGE);
        }else{
            atualizarTB();
        }
    }

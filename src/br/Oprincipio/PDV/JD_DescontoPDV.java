    private void valorKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_valorKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ESCAPE){
            this.dispose();
        }
        if (evt.getKeyCode() == KeyEvent.VK_ENTER){
            try{
            if (jRadioButton1.isSelected()){
                tipo = 1;
                v = (Double.parseDouble(valor.getText().replaceAll(",", "\\."))/100)+1;
                this.dispose();
            }
            if (jRadioButton2.isSelected()){
                tipo = 2;
                v = Conv.CDblDuasCasas(Double.parseDouble(valor.getText().replaceAll(",", "\\.")));
                this.dispose();
            }
            }catch(NumberFormatException ex){
                System.err.println(ex.getMessage());
            }
        }
    }//GEN-LAST:event_valorKeyPressed


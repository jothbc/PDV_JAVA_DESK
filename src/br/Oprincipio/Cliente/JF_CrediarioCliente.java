    private void verMovBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_verMovBtnActionPerformed
        if (jTable1.getSelectedRow() < 0) {
            return;
        }
        int movimento_temp = (int) jTable1.getValueAt(jTable1.getSelectedRow(), 0);
        for (Venda_Bean v : vendas) {
            if (v.getMovimento() == movimento_temp) {
                JD_Movimento jd = new JD_Movimento(null, true, v);
                jd.setVisible(true);
                break;
            }
        }
    }//GEN-LAST:event_verMovBtnActionPerformed


    private void buscar() {
        JD_BuscaCliente jd = new JD_BuscaCliente(null, true,true);
        jd.setVisible(true);
        if (jd.cliente != null) {
            cliente = jd.cliente;
            atualizarCliente();
            atualizarTb();
            credInjettf.setEnabled(true);
            jButton4.setEnabled(true);
            baixaSelecionadoBtn.setEnabled(true);
        }
    }

    private void atualizarCliente() {
        int id = cliente.getId();
        cliente = new Cliente_DAO().getCliente(id);
        nometf.setText(cliente.getNome());
        cpftf.setText(cliente.getCpf());
        creditotf.setText(Conv.validarValue(cliente.getCredito()));
    }

    private void cancelar() {
        cliente = null;
        nometf.setText("");
        cpftf.setText("");
        totaltf.setText("");
        creditotf.setText("");
        totalDevidotf.setText("");
        tb.setRowCount(0);
        credInjettf.setEnabled(false);
        jButton4.setEnabled(false);
        baixaSelecionadoBtn.setEnabled(false);
        vendas = null;
    }

    private void atualizarTb() {
        tb.setRowCount(0);
        vendas = new Venda_DAO().findAll_VendasPorCliente(cliente.getId(), true);
        double total = 0;
        for (Venda_Bean v : vendas) {
            if (!v.isPago()) {
                Object[] dado = {v.getMovimento(), v.getTotal(), v.getCrediario(), v.getData(), null};
                tb.addRow(dado);
                total += v.getCrediario();
            }
        }
        totaltf.setText(Conv.validarValue(total));
        totalDevidotf.setText(Conv.validarValue(Conv.CDblDuasCasas(total - cliente.getCredito())));
    }

    private void injetar_credito() {
        //obtem o valor inserido

        double injet = 0;
        try {
            injet = Double.parseDouble(credInjettf.getText().replaceAll(",", "\\."));
        } catch (NumberFormatException e) {
            System.out.println("valor informado para injetar inválido.\nEsperado um numero.");
            return;
        }
        injet = Conv.CDblDuasCasas(injet);

        if (new Cliente_DAO().injetarCredito(cliente.getId(), injet)) { //injeta o valor no credito do cliente
            atualizarCliente();
            atualizarTb();
            verificarSeTemCreditoSuficienteParaPagarConta();
            atualizarTb();
            injetarRestanteCrediarioEmContaAberta();
        }
        atualizarCliente();
        atualizarTb();
        if (injet > 0) {
            Historico_Cred hist = new Historico_Cred();
            hist.setCliente(cliente);
            hist.setData(CDate.DataPTBRAtual());
            hist.setValor(injet);
            new Historico_CredDAO().addHist(hist);
        }
        credInjettf.setText("");
    }

    private void verificarSeTemCreditoSuficienteParaPagarConta() {
        for (Venda_Bean v : vendas) { //obtem todas as vendas em aberto do cliente
            if (!v.isPago() && v.getCrediario() > 0 && v.getCrediario() <= cliente.getCredito()) { //se o crédito do cliente cobrir alguma das contas é automaticamente pago
                if (new Venda_DAO().pagarCrediario(v.getMovimento(), v.getCrediario())) {
                    new Cliente_DAO().debitarCredito(cliente.getId(), v.getCrediario());
                    atualizarCliente();
                } else {
                    JOptionPane.showMessageDialog(null, "Erro ao tentar debitar valor.");
                    break;
                }
            }
        }
    }

    private void injetarRestanteCrediarioEmContaAberta() {
        if (cliente.getCredito() > 0) {
            for (Venda_Bean v : vendas) { //obtem todas as vendas em aberto do cliente
                if (!v.isPago() && v.getCrediario() > cliente.getCredito()) { //injetar em uma venda ainda nao paga
                    if (new Venda_DAO().injetarCrediarioParcial(v.getSeq(), cliente.getCredito())) {
                        new Cliente_DAO().debitarCredito(cliente.getId(), cliente.getCredito());
                    } else {
                        JOptionPane.showMessageDialog(null, "Erro ao tentar debitar valor.");
                    }
                    return;
                }
            }
        }
    }

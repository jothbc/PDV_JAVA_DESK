package br.Oprincipio.Bean;

/**
 *
 * @author Jonathan
 */
public class Historico_Cred {

    private int id;
    private Cliente_Bean cliente;
    private double valor;
    private String data;

    /**
     * @return the id
     */
    public int getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(int id) {
        this.id = id;
    }

    /**
     * @return the cliente
     */
    public Cliente_Bean getCliente() {
        return cliente;
    }

    /**
     * @param cliente the cliente to set
     */
    public void setCliente(Cliente_Bean cliente) {
        this.cliente = cliente;
    }

    /**
     * @return the valor
     */
    public double getValor() {
        return valor;
    }

    /**
     * @param valor the valor to set
     */
    public void setValor(double valor) {
        this.valor = valor;
    }

    /**
     * @return the data
     */
    public String getData() {
        return data;
    }

    /**
     * @param data the data to set
     */
    public void setData(String data) {
        this.data = data;
    }

    public String toString() {
        return "[" + this.getId() + " : " + this.getCliente().getNome() + " | " + this.getData() + " | " + this.getValor() + "]";
    }

}
